import Chat from "../models/ChatSchema.js";
import { Types } from "mongoose";

/************************************************************************
 **************************** CREATE ************************************
 ************************************************************************/
export function create(payload) {
  return Chat.create({
    ...payload,
    project: new Types.ObjectId(payload.project),
    creator: new Types.ObjectId(payload.creator),
  });
}
