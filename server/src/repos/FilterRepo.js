import model from "../models/filterSchema.js";
import { ObjectId } from "../utils/ObjectId.js";

/************************************************************************
 **************************** CREATE ************************************
 ************************************************************************/
export async function create(payload) {

  const doc = await model.create({
    ...payload,
    project: ObjectId(payload.project),
  });
  
  return doc;
}

